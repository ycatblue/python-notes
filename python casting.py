"""
Python is an object-orientated language, and as such it uses classes to define data types,
including its primitive types.
"""

# int casting
a = int("2")
print(a)
print(type(a))

# str casting
b = str(2)
print(b)
print(type(b))

# float casting
c = float(3)
print(c)
print(type(c))
# The count() method returns the number of times a specified value appears in the string.
txt = "I love china,because I am a chinese"
x = txt.count("I")
print(x)

# Synatax : string.count(value, start, end)
a = "cat is cute,cat is lovely,cat is smart,cat is funny"
y = a.count("cat", 7, 30)
print(y)
